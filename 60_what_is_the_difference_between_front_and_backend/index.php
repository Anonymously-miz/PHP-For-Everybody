<h1>Frontend vs. Backend</h1>

Frontend:
HTML/CSS/JavaScript (not Node.js)

Backend:
Database, DevOps, PHP, MySQL, File, Python, Ruby, Java

Fullstack: 
Frontend + Backend
- FE: HTML/HTM5/CSS/CSS3/JavaScript (jQuery, React, Vue, Es678)
- BE: Python, PHP, Nginx or Apache. 

Do you write code for a server, or for a browser?